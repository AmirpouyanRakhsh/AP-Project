package com.company.Model;

public class Faculty {
    private String name;
    private String educationalAssistant;
    private String deanOfTheFaculty;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setEducationalAssistant(String educationalAssistant) {
        this.educationalAssistant = educationalAssistant;
    }

    public String getEducationalAssistant() {
        return educationalAssistant;
    }

    public void setDeanOfTheFaculty(String deanOfTheFaculty) {
        this.deanOfTheFaculty = deanOfTheFaculty;
    }

    public String getDeanOfTheFaculty() {
        return deanOfTheFaculty;
    }
}
